package org.example.chapter4_circulation.While;

public class try_While {
    public static void main(String[] args) {
        // 打印 1-100
        double num = 0.1;
        int count = 0;
        while (num <= 8844430.0){
            num*=2;
            count++;
        }
        System.out.println(count);
    }
}

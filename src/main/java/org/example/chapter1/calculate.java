package org.example.chapter1;

public class calculate {

    public static void main(String[] args) {
        String str1 = "张铭慧";
        String str2 = "超级可爱";
        System.out.println(str1 + str2);

        int x = 22;
        int y = 3;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(Math.pow(x,y));  //返回一个double类型
    }

}
